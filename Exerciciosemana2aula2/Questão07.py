"""Escreva um programa que solicite ao usuário o seu salário mensal e o número de meses trabalhados no ano. Calcule e imprima o salário anual."""

salário = float(input("Digite o seu Salário"))

meses = float(input("Digite o numero de meses trabalhados"))

recebeu = salário * meses

print("Você recebeu um salário anual de", recebeu )