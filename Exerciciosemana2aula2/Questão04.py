""" 4. Escreva um programa que solicite ao usuário dois números inteiros e armazene-os em variáveis. 
Em seguida, calcule e imprima a soma, subtração, multiplicação e divisão desses números."""

num1 = int(input ("Digite um número"))
num2 = int(input ("Digite um número"))

Soma = (num1) + (num2)
Subtração = (num1) - (num2)
Multiplicação = (num1) * (num2)
Divisão = (num1) / (num2)
print(Soma,Subtração,Multiplicação,Divisão)